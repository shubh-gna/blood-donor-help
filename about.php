              <h3>WHAT IS BLOOD?</h3>
              <p>Blood is a specialized body fluid. It has four main components: plasma, red blood cells, white blood cells, and platelets. Blood has many different functions, including:
              </p>
              <ul>
<li>transporting oxygen and nutrients to the lungs and tissues</li>
<li>forming blood clots to prevent excess blood loss</li>
<li>carrying cells and antibodies that fight infection</li>
<li>bringing waste products to the kidneys and liver, which filter and clean the blood</li>
<li>regulating body temperature</li>
</ul>
<p>
The blood that runs through the veins, arteries, and capillaries is known as whole blood, a mixture of about 55 percent plasma and 45 percent blood cells. About 7 to 8 percent of your total body weight is blood. An average-sized man has about 12 pints of blood in his body, and an average-sized woman has about nine pints. </p>
<br>

<h3>COMPONENTS OF BLOOD</h3>
<p>There are four basic components that comprise human blood: plasma, red blood cells, white blood cells and platelets.</p>
<img src="imgs/p1.png" alt="" style="max-width:100%">

<h5>
  Red Blood Cells
</h5>
<p>
  Red blood cells represent 40%-45% of your blood volume. They are generated from your bone marrow at a rate of four to five billion per hour. They have a lifecycle of about 120 days in the body.
  <p><h5>Platelets</h5>
    <p>
  Platelets are an amazing part of your blood. Platelets are the smallest of our blood cells and literally look like small plates in their non-active form. Platelets control bleeding. Wherever a wound occurs, the blood vessel will send out a signal. Platelets receive that signal and travel to the area and transform into their “active” formation, growing long tentacles to make contact with the vessel and form clusters to plug the wound until it heals.
  <p><h5>Plasma</h5>
    <p>
  Plasma is the liquid portion of your blood. Plasma is yellowish in color and is made up mostly of water, but it also contains proteins, sugars, hormones and salts. It transports water and nutrients to your body’s tissues.
  <p><h5>White Blood Cells</h5>
  <p>Although white blood cells (leukocytes) only account for about 1% of your blood, they are very important. White blood cells are essential for good health and protection against illness and disease. Like red blood cells, they are constantly being generated from your bone marrow. They flow through the bloodstream and attack foreign bodies, like viruses and bacteria. They can even leave the bloodstream to extend the fight into tissue.

</p>
