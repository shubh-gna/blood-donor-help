<h3>Food and medications</h3>
<p>Before your blood donation:</p>

<ul>
  <li>Get plenty of sleep the night before you plan to donate.</li>
<li>Eat a healthy meal before your donation. Avoid fatty foods, such as a hamburger, fries or ice cream.</li>
<li>Drink plenty of water before the donation.</li>
<li>Check to see if any medications you are taking or recently took would prevent you from donating. For example, if you are a platelet donor, you must not take aspirin for two days prior to donating. Talk to your health care provider before you stop taking any medications.</li>
<li>Wear a shirt with sleeves that can be rolled up.</li>
</ul>
<br>
<h3>What you can expect</h3>
<h5>Before the procedure</h5>
<p>Before you can donate blood, you will be asked to fill out a confidential medical history. It includes questions about behaviors known to carry a higher risk of bloodborne infections — infections that are transmitted through blood.
<br><br>Because of the risk of bloodborne infections, not everyone can donate blood. The following are groups that are not eligible to donate blood:</p>
<ul>
  <li>Anyone who has used injected drugs, steroids or another substance not prescribed by a health care provider in the past three months</li>
<li>Men who have had sexual contact with other men in the past three months</li>
<li>Anyone who has a congenital coagulation factor deficiency</li>
<li>Anyone who has had a positive test for HIV</li>
<li>Anyone who has engaged in sex for money or drugs in the past three months</li>
<li>Anyone who, in the past 12 months, has had close contact with — lived with or had sexual contact with — a person who has viral hepatitis</li>
<li>Anyone who has had babesiosis, a rare and severe tick-borne disease, or the parasitic infection Chagas' disease</li>
</ul>
<p>
You will also have a brief physical exam. The exam includes checking your blood pressure, pulse and temperature. A small sample of blood is taken from a finger prick and is used to check the oxygen-carrying part of your blood (hemoglobin level). If your hemoglobin concentration is within a healthy range, and you've met all the other screening requirements, you can donate blood.
</p>
<br>
<h5>After the procedure</h5>
<p>After donating, you sit in an observation area, where you rest and eat a light snack. After 15 minutes, you can leave. After your blood donation:</p>
<ul>
  <li>Drink extra fluids.</li>
<li>Avoid strenuous physical activity or heavy lifting for about five hours.</li>
<li>If you feel lightheaded, lie down with your feet up until the feeling passes.</li>
<li>Keep your bandage on and dry for the next five hours.</li>
<li>If you have bleeding after removing the bandage, put pressure on the site and raise your arm until the bleeding stops.</li>
<li>If bruising occurs, apply a cold pack to the area periodically during the first 24 hours.</li>
<li>Consider adding iron-rich foods to your diet to replace the iron lost with blood donation.</li>
</ul>
